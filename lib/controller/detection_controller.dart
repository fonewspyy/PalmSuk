  ssDrunModeOnStreamFram(CameraImage img) async {
    if (isprocessing) return;
    isprocessing = true;
    await Future.delayed(const Duration(microseconds: 500));
    result.value = "";
    try {
      imageHeight.value = img.height.toDouble();
      imageWidth.value = img.width.toDouble();
      recognitions.value = (await Tflite.detectObjectOnFrame(
        bytesList: img.planes.map((plan) {
          return plan.bytes;
        }).toList(),
        model: 'SSDMobileNet',
        imageHeight: img.height,
        imageWidth: img.width,
        imageMean: 127.5,
        imageStd: 127.5,
        rotation: 90,
        numResultsPerClass: 2,
        threshold: 0.1,
        asynch: true,
      ))!;

      // Reset count
      laptopCount.value = 0;
      phoneCount.value = 0;

      // // üîΩ ‡∏ß‡∏ô‡∏•‡∏π‡∏õ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏ä‡πá‡∏Å‡∏ß‡πà‡∏≤‡∏ï‡∏£‡∏ß‡∏à‡πÄ‡∏à‡∏≠ class ‡πÑ‡∏´‡∏ô
      for (var re in recognitions) {
        if (re["confidenceInClass"] >= 0.5) {
          if (re["detectedClass"] == "laptop") {
            laptopCount.value++;
          } else if (re["detectedClass"] == "keyboard") {
            phoneCount.value++;
          }
        }
      }
      // print(recognitions.value);
    } catch (e) {
    } finally {
      isprocessing = false;
    }
  }
  // ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏Å‡∏î‡∏õ‡∏∏‡πà‡∏° SAVE
  void savePalmData() {
    String currentDate = DateTime.now().toString().split(' ')[0]; // ‡πÄ‡∏Å‡πá‡∏ö‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô (‡∏õ‡∏µ-‡πÄ‡∏î‡∏∑‡∏≠‡∏ô-‡∏ß‡∏±‡∏ô)

    // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà‡∏•‡∏á‡πÉ‡∏ô‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£
    palmRecords.insert(0, {
      'date': currentDate,
      'laptop': laptopCount.value,  // laptop
      'keyboard': phoneCount.value, // keyboard
      'timestamp': DateTime.now().toString(),  // ‡πÄ‡∏ß‡∏•‡∏≤‡∏ó‡∏µ‡πà‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å
    });

    // ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏ú‡∏•‡∏£‡∏ß‡∏°
    totalLaptopCount.value += laptopCount.value;
    totalKeyboardCount.value += phoneCount.value;
  }

  // ‡∏õ‡∏∏‡πà‡∏° SEARCH ‡πÄ‡∏õ‡∏¥‡∏î-‡∏õ‡∏¥‡∏î‡∏Å‡∏•‡πâ‡∏≠‡∏á
  Future<void> toggleCamera() async {
    if (isInitialized.value) {
      // ‡∏õ‡∏¥‡∏î‡∏Å‡∏•‡πâ‡∏≠‡∏á
      if (isStreaming.value) {
        try {
          await cameraController.stopImageStream();
          isStreaming.value = false;
        } catch (e) {
          print("‚ö†Ô∏è stopImageStream ‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß: $e");
        }
      }

      await cameraController.dispose();
      isInitialized.value = false;
      isCameraRunning.value = false;
    } else {
      // ‡πÄ‡∏õ‡∏¥‡∏î‡∏Å‡∏•‡πâ‡∏≠‡∏á
      final cameras = await availableCameras();
      cameraController = CameraController(cameras[0], ResolutionPreset.medium);
      await cameraController.initialize();
      isInitialized.value = true;
      isCameraRunning.value = true;

      try {
        await cameraController.startImageStream(ssDrunModeOnStreamFram);
        isStreaming.value = true;
      } catch (e) {
        print("‚ö†Ô∏è startImageStream ‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß: $e");
      }
    }
  }

